<?php 
    echo "<h2>Assignment Operators</h2>";
    $x = 20; $y = 10;
    echo "\$x = $x and \$y = $y <br><br>";

    echo "Add and Assign:<br>";
    $x += $y; echo $x; //30

    echo "<br>Subtract and Assign:<br>";
    $x -= $y; echo $x; //20

    echo "<br>Multiply and Assign:<br>";
    $x *= $y; echo $x; //200

    echo "<br>Divide and Assign:<br>";
    $x /= $y; echo $x; //20

    echo "<br>Modulus and Assign:<br>";
    $x %= $y; echo $x; //0s